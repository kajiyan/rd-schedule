const NAV_DAY_ITEM_INPUT_CLASS_NAME_PREFIX = "js-nav-Day_ItemInput";
const NAV_HOUR_ITEM_INPUT_CLASS_NAME_PREFIX = "js-nav-Hour_ItemInput";

const LABEL = {
  DAY: {
    MON: "月曜日",
    TUE: "火曜日",
    WED: "水曜日",
    THU: "木曜日",
    SAT: "金曜日",
  },
  HOUR: ["1・2限", "1・2限", "3・4限", "3・4限", "5・6限", "5・6限"],
};

const state = {
  day: "tue",
  hour: 1,
};

const navDayItemInputList = document.querySelectorAll(
  `.${NAV_DAY_ITEM_INPUT_CLASS_NAME_PREFIX}`
);
const navHourItemInputList = document.querySelectorAll(
  `.${NAV_HOUR_ITEM_INPUT_CLASS_NAME_PREFIX}`
);
const map = document.querySelector("#js-map");
const mapDay = map.querySelector("#js-map-Day");
const mapHour = map.querySelector("#js-map-Hour");
const mapAreaList = map.querySelectorAll(".js-map-Area");
const mapAreaEntity = {};
// const mapAreaEntityIds = [];
const listTableTrList = document.querySelectorAll(".js-list-Table_Tr");
const listTableTrEntity = {};
// const listTableTrEntityIds = [];

navDayItemInputList.forEach(navDayItemInput => {
  if (
    navDayItemInput.id ===
    `${NAV_DAY_ITEM_INPUT_CLASS_NAME_PREFIX}-${state.day}`
  ) {
    navDayItemInput.checked = true;
  }
});

navHourItemInputList.forEach(navHourItemInput => {
  if (
    navHourItemInput.id ===
    `${NAV_HOUR_ITEM_INPUT_CLASS_NAME_PREFIX}-${state.hour.toString()}`
  ) {
    navHourItemInput.checked = true;
  }
});

mapAreaList.forEach(mapArea => {
  const { id } = mapArea;
  
  // mapAreaEntityIds.push(id);

  mapAreaEntity[id] = {
    root: mapArea,
    lectureEl: mapArea.querySelector(".js-map-Area_Lecture"),
  };
});

listTableTrList.forEach(listTableTr => {
  const { id } = listTableTr;
  
  // listTableTrEntityIds.push(id);

  listTableTrEntity[id] = {
    root: listTableTr,
    teacherEl: listTableTr.querySelector(".js-list-Table_Td-teacher"),
    lectureEl: listTableTr.querySelector(".js-list-Table_Td-lecture"),
  };
});

/**
 * updateMap
 */
const updateMap = (locationId, scheduleEntity) => {
  const entityId = `js-map-Area-${locationId}`;
  const entity = mapAreaEntity[entityId];

  if (typeof entity === "undefined") {
    return;
  }

  const { lectureEl } = entity;
  const { lecture } = scheduleEntity;

  lectureEl.textContent = lecture;
  entity.root.classList.remove("map-Area-active");

  if (lecture.length > 0) {
    entity.root.classList.add("map-Area-active");
  }
};

/**
 * updateTable
 */
const updateTable = (locationId, scheduleEntity) => {
  const entityId = `js-list-Table_Tr-${locationId}`;
  const { root, teacherEl, lectureEl } = listTableTrEntity[entityId];
  const { teacher, lecture } = scheduleEntity;

  root.classList.remove("list-Table_Tr-hide");

  if (teacher === "" && lecture === "") {
    root.classList.add("list-Table_Tr-hide");
  }

  teacherEl.textContent = teacher;
  lectureEl.textContent = lecture;
};

/**
 * update
 */
const update = () => {
  const { day, hour } = state;

  mapDay.textContent = LABEL.DAY[day.toUpperCase()];
  mapHour.textContent = LABEL.HOUR[hour];

  for (let [locationId, schedule] of Object.entries(SCHEDULE)) {
    const scheduleEntity = schedule[day][hour];

    updateMap(locationId, scheduleEntity);
    updateTable(locationId, scheduleEntity);
  }
};

/**
 * onDayChange
 */
const onDayChange = (e) => {
  const day = e.srcElement.value;

  state.day = day;

  update();
};

/**
 * onHourChange
 */
const onHourChange = (e) => {
  const hour = e.srcElement.value;

  state.hour = parseInt(hour, 10);

  update();
};

navDayItemInputList.forEach((navDayItemInput) => {
  navDayItemInput.addEventListener('change', onDayChange);
});

navHourItemInputList.forEach((navHourItemInput) => {
  navHourItemInput.addEventListener("change", onHourChange);
});

update();

